// Operadores matemáticos
a = 10
b = 5

soma = a + b
substracao = a - b
multi = a * b
div = a / b
resDiv = a % b

function mensage(operacao, resultado){
    console.log(`a ${operacao} b  = ${resultado}`)
}
mensage("+", soma)
mensage("-", substracao)
mensage("*", multi)
mensage("/", div)
mensage("%", resDiv)


